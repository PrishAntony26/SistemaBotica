    private void ListaMesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ListaMesActionPerformed
        // TODO add your handling code here:
        jLabelTotal.setText("");
        int numMes = ListaMes.getSelectedIndex() + 1;

        modelo.setRowCount(0);
        
        int cantVentas = Botica.vent.cantElementos();
        double total=0;
        
        for(int i = 0; i < cantVentas; i++){
            if(venta[i].getMes() == numMes){
                DetalleVenta[] detAux;
                detAux = venta[i].obtenerArrayListaDetalleVenta();
                
                int cantDetalleVenta = venta[i].contadorDetalleVenta();
                
                for(int j=0; j<cantDetalleVenta;j++){
                    Medicamento m = detAux[j].getM();
                    String[] newRow={m.getNombre(), Double.toString(detAux[j].getSubtotal())};
                    modelo.addRow(newRow);
                    
                    total = total + detAux[j].getSubtotal();  
                }
                jLabelTotal.setText(Double.toString(total));       
            }
        }
    }//GEN-LAST:event_ListaMesActionPerformed

